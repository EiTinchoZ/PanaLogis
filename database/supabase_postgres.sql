CREATE OR REPLACE FUNCTION sp_crear_orden(
    p_fecha_prog date,
    p_id_cliente integer,
    p_id_ruta integer,
    p_id_vehiculo integer,
    p_id_conductor integer,
    p_id_tipo_carga integer,
    p_peso_kg numeric(10, 2),
    p_descripcion text
)
RETURNS TABLE (numero_orden varchar(30), mensaje varchar(200))
LANGUAGE plpgsql
AS $$
DECLARE
    v_id_orden integer;
BEGIN
    v_id_orden := nextval(pg_get_serial_sequence('orden_servicio', 'id_orden'));
    numero_orden := 'ORD-' || EXTRACT(YEAR FROM CURRENT_DATE)::int || '-' || LPAD(v_id_orden::text, 6, '0');

    INSERT INTO orden_servicio (
        id_orden,
        numero_orden,
        fecha_programada,
        id_cliente,
        id_ruta,
        id_vehiculo,
        id_conductor,
        id_tipo_carga,
        peso_kg,
        descripcion
    )
    VALUES (
        v_id_orden,
        numero_orden,
        p_fecha_prog,
        p_id_cliente,
        p_id_ruta,
        p_id_vehiculo,
        p_id_conductor,
        p_id_tipo_carga,
        p_peso_kg,
        p_descripcion
    );

    mensaje := 'Orden creada exitosamente: ' || numero_orden;
    RETURN NEXT;
END;
$$;

CREATE OR REPLACE FUNCTION sp_rentabilidad_ruta(
    p_anio integer,
    p_mes integer
)
RETURNS TABLE (
    ruta varchar(150),
    origen varchar(200),
    destino varchar(200),
    total_servicios bigint,
    ingresos_netos numeric,
    itbms_cobrado numeric,
    ingresos_totales numeric,
    promedio_por_servicio numeric
)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT
        r.nombre::varchar(150) AS ruta,
        r.origen::varchar(200),
        r.destino::varchar(200),
        COUNT(o.id_orden) AS total_servicios,
        SUM(f.subtotal) AS ingresos_netos,
        SUM(f.impuesto) AS itbms_cobrado,
        SUM(f.total) AS ingresos_totales,
        AVG(f.total) AS promedio_por_servicio
    FROM ruta r
    JOIN orden_servicio o ON o.id_ruta = r.id_ruta
    JOIN factura f ON f.id_orden = o.id_orden
                 AND f.estado <> 'ANULADA'
    WHERE o.estado = 'ENTREGADO'
      AND (p_anio IS NULL OR EXTRACT(YEAR FROM o.fecha_programada) = p_anio)
      AND (p_mes IS NULL OR EXTRACT(MONTH FROM o.fecha_programada) = p_mes)
    GROUP BY r.id_ruta, r.nombre, r.origen, r.destino
    ORDER BY ingresos_totales DESC;
END;
$$;

