CREATE TYPE vehiculo_estado_enum AS ENUM ('ACTIVO', 'MANTENIMIENTO', 'INACTIVO');
CREATE TYPE conductor_estado_enum AS ENUM ('ACTIVO', 'INACTIVO', 'SUSPENDIDO');
CREATE TYPE cliente_estado_enum AS ENUM ('ACTIVO', 'INACTIVO');
CREATE TYPE usuario_estado_enum AS ENUM ('ACTIVO', 'INACTIVO');
CREATE TYPE orden_estado_enum AS ENUM ('PENDIENTE', 'EN_TRANSITO', 'ENTREGADO', 'CANCELADO');
CREATE TYPE mantenimiento_tipo_enum AS ENUM ('PREVENTIVO', 'CORRECTIVO', 'REVISION');
CREATE TYPE mantenimiento_estado_enum AS ENUM ('EN_PROCESO', 'COMPLETADO');
CREATE TYPE factura_estado_enum AS ENUM ('PENDIENTE', 'PAGADA', 'ANULADA');
CREATE TYPE bitacora_operacion_enum AS ENUM ('INSERT', 'UPDATE', 'DELETE');

CREATE TABLE tipo_vehiculo (
    id_tipo_vehiculo integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descripcion varchar(100) NOT NULL,
    capacidad_ton numeric(8, 2) NOT NULL
);

CREATE TABLE vehiculo (
    id_vehiculo integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    placa varchar(20) NOT NULL UNIQUE,
    marca varchar(80) NOT NULL,
    modelo varchar(80) NOT NULL,
    anio smallint NOT NULL CHECK (anio BETWEEN 1900 AND 2100),
    id_tipo_vehiculo integer NOT NULL REFERENCES tipo_vehiculo(id_tipo_vehiculo),
    estado vehiculo_estado_enum NOT NULL DEFAULT 'ACTIVO',
    kilometraje numeric(10, 2) NOT NULL DEFAULT 0
);

CREATE TABLE conductor (
    id_conductor integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cedula varchar(20) NOT NULL UNIQUE,
    nombre varchar(100) NOT NULL,
    apellido varchar(100) NOT NULL,
    telefono varchar(20),
    email varchar(150),
    licencia varchar(50) NOT NULL UNIQUE,
    categoria_licencia varchar(10) NOT NULL,
    vences_licencia date NOT NULL,
    estado conductor_estado_enum NOT NULL DEFAULT 'ACTIVO'
);

CREATE TABLE cliente (
    id_cliente integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ruc varchar(30) NOT NULL UNIQUE,
    razon_social varchar(200) NOT NULL,
    contacto_nombre varchar(150),
    contacto_tel varchar(20),
    contacto_email varchar(150),
    direccion text,
    estado cliente_estado_enum NOT NULL DEFAULT 'ACTIVO'
);

CREATE TABLE tipo_carga (
    id_tipo_carga integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre varchar(100) NOT NULL,
    refrigeracion boolean NOT NULL DEFAULT false,
    es_peligrosa boolean NOT NULL DEFAULT false,
    observaciones text
);

CREATE TABLE ruta (
    id_ruta integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre varchar(150) NOT NULL,
    origen varchar(200) NOT NULL,
    destino varchar(200) NOT NULL,
    distancia_km numeric(8, 2),
    tiempo_est_horas numeric(5, 2),
    tarifa_base numeric(10, 2) NOT NULL
);

CREATE TABLE rol_usuario (
    id_rol integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre varchar(80) NOT NULL UNIQUE,
    descripcion text
);

CREATE TABLE usuario (
    id_usuario integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username varchar(80) NOT NULL UNIQUE,
    nombre_completo varchar(200) NOT NULL,
    email varchar(150) NOT NULL UNIQUE,
    id_rol integer NOT NULL REFERENCES rol_usuario(id_rol),
    estado usuario_estado_enum NOT NULL DEFAULT 'ACTIVO',
    fecha_creacion timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE orden_servicio (
    id_orden integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    numero_orden varchar(30) NOT NULL UNIQUE,
    fecha_creacion timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    fecha_programada date NOT NULL,
    id_cliente integer NOT NULL REFERENCES cliente(id_cliente),
    id_ruta integer NOT NULL REFERENCES ruta(id_ruta),
    id_vehiculo integer NOT NULL REFERENCES vehiculo(id_vehiculo),
    id_conductor integer NOT NULL REFERENCES conductor(id_conductor),
    id_tipo_carga integer NOT NULL REFERENCES tipo_carga(id_tipo_carga),
    peso_kg numeric(10, 2) NOT NULL,
    descripcion text,
    estado orden_estado_enum NOT NULL DEFAULT 'PENDIENTE',
    observaciones text
);

CREATE TABLE mantenimiento (
    id_mantenimiento integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_vehiculo integer NOT NULL REFERENCES vehiculo(id_vehiculo),
    tipo mantenimiento_tipo_enum NOT NULL,
    fecha_inicio date NOT NULL,
    fecha_fin date,
    descripcion text NOT NULL,
    costo numeric(10, 2),
    taller varchar(200),
    estado mantenimiento_estado_enum NOT NULL DEFAULT 'EN_PROCESO'
);

CREATE TABLE factura (
    id_factura integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    numero_factura varchar(30) NOT NULL UNIQUE,
    id_orden integer NOT NULL UNIQUE REFERENCES orden_servicio(id_orden),
    fecha_emision timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    subtotal numeric(12, 2) NOT NULL,
    impuesto numeric(12, 2) NOT NULL,
    total numeric(12, 2) NOT NULL,
    estado factura_estado_enum NOT NULL DEFAULT 'PENDIENTE',
    fecha_pago date
);

CREATE TABLE bitacora (
    id_bitacora integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tabla_afectada varchar(100) NOT NULL,
    operacion bitacora_operacion_enum NOT NULL,
    id_registro integer NOT NULL,
    descripcion text,
    fecha_operacion timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    id_usuario integer REFERENCES usuario(id_usuario)
);

CREATE OR REPLACE FUNCTION fn_check_conductor_libre()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM orden_servicio
        WHERE id_conductor = NEW.id_conductor
          AND estado IN ('PENDIENTE', 'EN_TRANSITO')
    ) THEN
        RAISE EXCEPTION 'El conductor ya tiene una orden activa asignada.' USING ERRCODE = 'P0001';
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_check_vehiculo_disponible()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM vehiculo
        WHERE id_vehiculo = NEW.id_vehiculo
          AND estado = 'MANTENIMIENTO'
    ) THEN
        RAISE EXCEPTION 'El vehículo está en mantenimiento y no puede operar.' USING ERRCODE = 'P0001';
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_generar_factura()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    v_tarifa numeric(10, 2);
    v_subtotal numeric(12, 2);
    v_num varchar(30);
BEGIN
    IF NEW.estado = 'ENTREGADO' AND OLD.estado IS DISTINCT FROM 'ENTREGADO' THEN
        SELECT tarifa_base
        INTO v_tarifa
        FROM ruta
        WHERE id_ruta = NEW.id_ruta;

        v_subtotal := v_tarifa * (NEW.peso_kg / 1000);
        v_num := 'FAC-' || EXTRACT(YEAR FROM CURRENT_DATE)::int || '-' || LPAD(NEW.id_orden::text, 6, '0');

        INSERT INTO factura (numero_factura, id_orden, subtotal, impuesto, total)
        VALUES (v_num, NEW.id_orden, v_subtotal, v_subtotal * 0.07, v_subtotal * 1.07)
        ON CONFLICT (id_orden) DO NOTHING;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_bitacora_orden()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF OLD.estado IS DISTINCT FROM NEW.estado THEN
        INSERT INTO bitacora (tabla_afectada, operacion, id_registro, descripcion)
        VALUES (
            'ORDEN_SERVICIO',
            'UPDATE',
            NEW.id_orden,
            'Estado cambio de [' || OLD.estado || '] a [' || NEW.estado || ']'
        );
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_vehiculo_a_mantenimiento()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.estado = 'EN_PROCESO' THEN
        UPDATE vehiculo
        SET estado = 'MANTENIMIENTO'
        WHERE id_vehiculo = NEW.id_vehiculo;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_vehiculo_liberado()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.estado = 'COMPLETADO' AND OLD.estado = 'EN_PROCESO' THEN
        UPDATE vehiculo
        SET estado = 'ACTIVO'
        WHERE id_vehiculo = NEW.id_vehiculo;
    END IF;

    RETURN NEW;
END;
$$;

